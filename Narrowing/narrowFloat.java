class narrowFloat 
{
	public static void main(String[] args) 
	{
		float f=233.87345f;

        long l=(long)f;
		int i=(int)f;
		char c=(char)f;
		short s=(short)f;
		byte b=(byte)f;

		System.out.println(l);
		System.out.println(i);
		System.out.println(c);
		System.out.println(s);
		System.out.println(b);
	}
}
